/* ── Language toggle ───────────────────────────────────────────── */
let currentLang = 'en';

function setLanguage(lang) {
    currentLang = lang;

    // Update all text elements
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (i18n[lang][key] !== undefined) {
            el.innerHTML = i18n[lang][key];
        }
    });

    // Update all placeholders
    document.querySelectorAll('[data-i18n-ph]').forEach(el => {
        const key = el.getAttribute('data-i18n-ph');
        if (i18n[lang][key] !== undefined) {
            el.placeholder = i18n[lang][key];
        }
    });

    // Update toggle button labels
    document.getElementById('langToggle').textContent       = lang === 'en' ? 'JP' : 'EN';
    document.getElementById('mobileLangToggle').textContent = lang === 'en' ? 'EN | JP' : 'JP | EN';

    // Update html lang attribute
    document.documentElement.lang = lang === 'en' ? 'en' : 'ja';
}

document.getElementById('langToggle').addEventListener('click', (e) => {
    e.preventDefault();
    setLanguage(currentLang === 'en' ? 'jp' : 'en');
});

document.getElementById('mobileLangToggle').addEventListener('click', (e) => {
    e.preventDefault();
    setLanguage(currentLang === 'en' ? 'jp' : 'en');
});

document.getElementById('footerLangEN').addEventListener('click', (e) => {
    e.preventDefault();
    setLanguage('en');
});

document.getElementById('footerLangJP').addEventListener('click', (e) => {
    e.preventDefault();
    setLanguage('jp');
});

